export function getAllKeywords() {
  return Object.values(VENDING_TYPES).flatMap(type => type.pools.flat());
}

export function drawKeywords(typeKey) {
  const type = VENDING_TYPES[typeKey];
  if (!type) throw new Error(`Unknown vending type: ${typeKey}`);
  return type.pools.map(pool => pool[Math.floor(Math.random() * pool.length)]);
}
