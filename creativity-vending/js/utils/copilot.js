import { VENDING_TYPES } from './keywords.js';

export function createDraft(item) {
  const type = VENDING_TYPES[item.vending_type];
  const typeName = type?.name ?? '아이디어';
  const axes = type?.axes ?? ['키워드 1', '키워드 2', '키워드 3'];
  const keywords = Array.isArray(item.keywords) ? item.keywords : [];
  const memo = item.user_memo?.trim() || '아직 메모가 없으므로 키워드 조합을 중심으로 확장한다.';
  const [a = '핵심 배경', b = '핵심 대상', c = '핵심 장치'] = keywords;

  if (item.vending_type === 'reels') {
    return [
      `# ${typeName} 기획안 초안`,
      '',
      `## 1. 한 줄 콘셉트`,
      `${a}를 ${b}으로 보여주되, 전체 톤은 ${c}로 가져가는 짧은 세로형 콘텐츠.`,
      '',
      `## 2. 메모 반영`,
      memo,
      '',
      `## 3. 구성`,
      `- 오프닝: 1초 안에 ${a}의 상황을 보여주는 강한 장면`,
      `- 본문: ${b} 구조로 정보나 변화를 빠르게 제시`,
      `- 마무리: ${c} 분위기에 맞는 한 문장 CTA`,
      '',
      `## 4. 제작 체크리스트`,
      `- 첫 컷 문구 3개 후보 만들기`,
      `- 촬영 장면 5개로 쪼개기`,
      `- 저장/공유를 부르는 마지막 문장 넣기`,
    ].join('\n');
  }

  if (item.vending_type === 'solution') {
    return [
      `# ${typeName} 기획안 초안`,
      '',
      `## 1. 문제 정의`,
      `${a}에 관심 있는 사용자는 "${b}"라는 문제를 반복해서 겪는다.`,
      '',
      `## 2. 솔루션 콘셉트`,
      `${c} 형태로 사용자의 행동을 작게 쪼개고, 기록과 피드백을 통해 지속하게 돕는다.`,
      '',
      `## 3. 메모 반영`,
      memo,
      '',
      `## 4. MVP 기능`,
      `- 첫 사용자가 바로 시작할 수 있는 3단계 온보딩`,
      `- 오늘 할 일/기록/피드백을 한 화면에서 처리`,
      `- 사용자의 누적 기록을 카드 형태로 저장`,
      '',
      `## 5. 다음 검증`,
      `- 타깃 사용자 5명에게 문제 공감도 인터뷰`,
      `- ${c} 형태가 실제 사용 맥락에 맞는지 테스트`,
    ].join('\n');
  }

  return [
    `# ${typeName} 기획안 초안`,
    '',
    `## 1. 핵심 조합`,
    `- ${axes[0]}: ${a}`,
    `- ${axes[1]}: ${b}`,
    `- ${axes[2]}: ${c}`,
    '',
    `## 2. 한 줄 로그라인`,
    `${a}에서 ${b}가 ${c}를 겪으며, 자신이 믿던 규칙을 다시 정의하는 이야기.`,
    '',
    `## 3. 메모 반영`,
    memo,
    '',
    `## 4. 기획 방향`,
    `- 초반: ${a}의 규칙과 결핍을 선명하게 보여준다.`,
    `- 중반: ${b}가 ${c} 때문에 기존 선택지를 잃는다.`,
    `- 후반: 메모의 아이디어를 해결 방식이나 반전 장치로 사용한다.`,
    '',
    `## 5. 다음 작업`,
    `- 주인공의 욕망과 두려움 1개씩 정의`,
    `- ${c}가 발생한 원인을 3가지 후보로 작성`,
    `- 첫 장면 또는 첫 에피소드 시놉시스 5문장 작성`,
  ].join('\n');
}
